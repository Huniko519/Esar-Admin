    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $jobs = Jobs::find($id);

           $jobs->queue = $input['queue'];$jobs->payload = $input['payload'];$jobs->attempts = $input['attempts'];$jobs->reserved_at = $input['reserved_at'];$jobs->available_at = $input['available_at'];$jobs->created_at = $input['created_at'];

            $res = $jobs->update();
            if ($res) {
                return response([
                    'status' => 'success',
                    'code' => 1,
                    'data' => $jobs
                ], 200);
            }
            return response([
                'status' => 'error',
                'code' => 0,
