    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $reports = Reports::find($id);

           $reports->user_reported = $input['user_reported'];$reports->car_id = $input['car_id'];$reports->inappropriate = $input['inappropriate'];$reports->misleading = $input['misleading'];$reports->spam = $input['spam'];$reports->other = $input['other'];$reports->reason = $input['reason'];$reports->created_at = $input['created_at'];$reports->updated_at = $input['updated_at'];

            $res = $reports->update();
