    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $permissions = Permissions::find($id);

           $permissions->name = $input['name'];$permissions->slug = $input['slug'];$permissions->description = $input['description'];$permissions->model = $input['model'];$permissions->created_at = $input['created_at'];$permissions->updated_at = $input['updated_at'];

            $res = $permissions->update();
            if ($res) {
                return response([
                    'status' => 'success',
                    'code' => 1,
                    'data' => $permissions
                ], 200);
            }
