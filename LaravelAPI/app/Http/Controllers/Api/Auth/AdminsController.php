    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $admins = Admins::find($id);

           $admins->name = $input['name'];$admins->email = $input['email'];$admins->password = $input['password'];$admins->created_at = $input['created_at'];$admins->updated_at = $input['updated_at'];

            $res = $admins->update();
            if ($res) {
